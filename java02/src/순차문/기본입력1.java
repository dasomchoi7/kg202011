package 순차문;

public class 기본입력1 {

	public static void main(String[] args) {
		char pass1 = 'p';
		char pass2 = 'q';
		
		if(pass1 == pass2) {//조건이 맞으면,  true!!
			System.out.println("PASS!");
			System.out.println("합겹입니다..!!");
			} else { //조건이 안 맞으면, false!!
				System.out.println("재입력!");
			}
		//[]대가로 / {}중가로 - 어디부터 어디까지다, 하나 일때는 중간 가로 안 써도 됨 /()소가로 - 명령문 조건문 쓸때
		
		
		
	}

}
